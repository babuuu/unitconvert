'''
A python module for converting distance between miles and kilometers
'''
def miles_to_kilometers(miles):
    '''
    CONVERTS MILES TO KILOMETERS
    
    Parameters:
    -----------
    miles: float
        A distance in miles
    Returns:
    --------
    km: float
    '''
    
    return(miles*1.60934)

